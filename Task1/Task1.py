print("Enter first number:");
firstNumber= input();

print("Enter second number:");
secondNumber= input();

print ("Enter operation:");
operation= input();

if operation =="+":
  print("Result:", firstNumber, "+", secondNumber, "=", float(firstNumber) + float(secondNumber));
if operation =="-":
  print("Result:", firstNumber, "-", secondNumber, "=", float(firstNumber) - float(secondNumber));
if operation =="*":
  print("Result:", firstNumber, "*", secondNumber, "=", float(firstNumber) * float(secondNumber));
if operation =="/":
  print("Result:", firstNumber, "/", secondNumber, "=", float(firstNumber) / float(secondNumber));


  



    







